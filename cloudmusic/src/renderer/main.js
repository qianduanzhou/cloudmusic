Vue.filter('addZero',function(value) {
    if(value / 10 < 1) {
      return "0" + value
  }
  return value
})

Vue.filter('time',function(value) {
  let seconds = Math.floor(value / 1000 % 60)
  if (seconds / 10 < 1) {
      seconds = "0" + seconds
  }
  let minute = Math.floor(value / 1000 / 60)
  return `0${minute}:${seconds}`
})

